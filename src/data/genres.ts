// ─── HELPER FUNCTIONS ────────────────────────────────────────────────────────

export function getGenreById(id: string): Genre | undefined {
  return GENRES.find((g) => g.id === id);
}

export function getCombinationSliders(mainId: string, subId: string): CombinationSliders | undefined {
  return COMBINATION_SLIDERS.find(
    (c) => c.mainGenreId === mainId && c.subgenreId === subId
  );
}

export function getEffectiveSliders(
  mainGenre: Genre,
  subgenreId?: string | null
): { sliders: GameSliders; alignment: AlignmentSliders } {
  if (subgenreId) {
    const combo = getCombinationSliders(mainGenre.id, subgenreId);
    if (combo) {
      return { sliders: combo.sliders, alignment: combo.alignment };
    }
  }
  return { sliders: mainGenre.sliders, alignment: mainGenre.alignment };
}
